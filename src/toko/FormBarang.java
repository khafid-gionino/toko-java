    public void viewdata(){
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("ID");
        tabel.addColumn("NAMA");
        tabel.addColumn("JENIS");
        tabel.addColumn("HARGA");
        tabel.addColumn("STOK");
        try{
            String sql = "select * from barang,jenis where barang.id_jenis = jenis.id order by barang.id asc";
            Toko.ps = Toko.conn.prepareStatement(sql);
            Toko.rs = Toko.ps.executeQuery(sql);
            while (Toko.rs.next()){
                tabel.addRow(new Object[]{
                    Toko.rs.getString(1),
                    Toko.rs.getString(3),
                    Toko.rs.getString(7),
                    Toko.rs.getString(4),
                    Toko.rs.getString(5),
                });
            }
            TableM.setModel(tabel);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane, "Data tidak ditemukan");
        }
    }
    
    public void jenis(){
        try {
            Connection con = Koneksi.getConnection();
            Statement stt = con.createStatement();
            String sql = "select nama from jenis";
            ResultSet res = stt.executeQuery(sql);
            
            while(res.next()){
            Object[] ob = new Object[3];
            ob[0] = res.getString(1);
            
            jComboBox1.addItem((String) ob[0]);
        }
        res.close(); 
        stt.close();
        } catch (Exception e) {
            
        }
    }

