    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //TODO add your handling code here:
        int idJenis = 0;
        int id = Integer.parseInt(ID.getText());
        String nama = Nama.getText();
        int harga = Integer.parseInt(Harga.getText());
        int stok = Integer.parseInt(Stok.getText());
        if(jComboBox1.getSelectedItem().equals("Pensil")){
            idJenis = 1;
        }
        else if (jComboBox1.getSelectedItem().equals("Makanan Ringan")){
            idJenis = 2;
        }
        Barang baru = new Barang();
        baru.input(id,idJenis, nama, harga, stok);
        viewdata();
    }//GEN-LAST:event_jButton2ActionPerformed

